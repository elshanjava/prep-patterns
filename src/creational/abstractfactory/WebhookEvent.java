package creational.abstractfactory;

/**
 * Доменное событие вебхука платёжного провайдера.
 *
 * Раньше тут в роли «события» использовался jdk.jfr.Event — служебный класс
 * профилировщика Java Flight Recorder, к платежам отношения не имеющий. Это
 * сбивало с толку, поэтому ввели понятный доменный тип. Сам факт замены к
 * паттерну не относится — он просто делает демонстрацию читаемой.
 *
 * @param type    нормализованный тип события (напр. "payment.succeeded")
 * @param payload сырое тело, как его прислал провайдер
 */
public record WebhookEvent(String type, String payload) {}
